                <div class="sous_titre_corps">
                    <h4>La PMEV : Pédagogie de Maîtrise à Effet Vicariant</h4>
                </div>
                <div class="sous_titre_corps">
                    <h5>Principe</h5>
                </div>
                <p><em>Maîtrise</em> : prise en compte des différences individuelles dans les apprentissages <em>Vicariant</em> : par observation de son environnement (pairs, supports divers, enseignant)</p>
                <p>Pratique pédagogique déjà éprouvée, la P.M.E.V. intervient sur <strong>deux leviers</strong> : en modifiant d’une part le rapport de l’élève au savoir et d’autre part celui de l’enseignant avec ses élèves elle crée un cadre de travail motivant et responsabilisant. L’élève est acteur de ses apprentissages, il est impliqué, il a l’initiative de l’appropriation. L’enseignant est à la fois tuteur en se positionnant au plus près des démarches et des besoins de chacun, et personne ressource et chef d’orchestre lorsqu’il anime les échanges.</p>
                <p>La P.M.E.V. mobilise <strong>deux  démarches</strong> :</p>
                <ul>
                    <li>L’autonomie dans la gestion du temps et des tâches : l’élève évalue ses besoins et compétences, se fixe des objectifs, choisit son organisation, bénéficie du droit à l’erreur.</li>
                    <li>La recherche d’indices pour construire les apprentissages : par l’intermédiaire d’exercices d’apprentissage et non d’application, l’élève, face à une situation problème, recherche des indices et propose ses solutions.</li>
                </ul>
                <div class="sous_titre_corps">
                    <h5>Organisation</h5>
                </div>
                <p>Son organisation s’articule autour de <strong>deux espaces temps</strong> :</p>
                <ul>
                    <li>Les compétences sont abordées par <strong>période de 3 semaines</strong> : cette durée correspond à un juste équilibre entre la quantité des notions à aborder et le temps de maturation nécessaire à leur acquisition.</li>
                    <li>Les <strong>séances</strong> quotidiennes se composent de deux modules : travail individualisé et phase de bilan.</li>
                </ul>
                <p>L’outil de l’élève est son <strong>plan de travail</strong> qui regroupe un ensemble de compétences liées aux divers champs disciplinaires. Chaque compétence est déclinée en plusieurs activités ou exercices qui vont conduire l’élève des phases de recherche plus ou moins complexes aux phases de construction, structuration puis approfondissement.</p>
                <p>L’élève bénéficie d’une grande liberté quant au choix de la compétence travaillée, l’ordre dans lequel il fait les exercices, le nombre d’exercices proposés à la correction. Mais l’objectif  incontournable reste la production « d’auto évaluation » de fin de période, repère en vue de l’évaluation commune.</p>
                <div class="sous_titre_corps">
                    <h5>L’évaluation</h5>
                </div>
                <p>Chaque période d’apprentissage est strictement évaluée : évaluation en continu de la période (n-1) au cours de la période n. Chacun est évalué sur les compétences disciplinaires qu’il a abordées pendant la période de référence.</p>
                <div class="sous_titre_corps">
                    <h4>Avantages de la P.M.E.V.</h4>
                </div>
                <p>La liberté donnée aux enfants assouplit le cadre scolaire. La gestion autonome des apprentissages responsabilise l’élève. En intervenant devant ses pairs il gagne en confiance et gère émotion et échecs éventuels. Il est toujours occupé donc ne subit ni répétition ni ennui qui ne soient souhaités. Il développe l’aptitude à l’écoute et l’auto-évaluation.</p>
