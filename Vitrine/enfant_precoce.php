		<p>Un enfant surdoué ou précoce a la particularité d’avoir un QI (quotient intellectuel) supérieur à 125 ou 130
			selon les spécialistes. Le QI permet de comparer le fonctionnement intellectuel d’un enfant par rapport aux
			enfants du même âge. L’enfant précoce se manifeste par de grandes capacités dans certains domaines mais
			aussi un mode de pensée et une structure de raisonnement différents. Il se caractérise surtout par un
			décalage plus ou moins important entre son développement intellectuel et son développement affectif,
			relationnel et psychomoteur. Cette « dysharmonie » ou « dyssynchronie » est perceptible et génératrice de
			troubles chez l’enfant à partir d’un QI de 125.</p>

		<p>D’une sensibilité extrême il capte tout ce qui l’entoure et en particulier l’état émotionnel d’autrui.</p>

