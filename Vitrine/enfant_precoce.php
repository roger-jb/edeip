		<p>Alors que l’école privilégie les compétences de l’hémisphère gauche du cerveau, l’enfant surdoué manifeste
			une prédominance de l’hémisphère droit, partie du cerveau « en images ». Les informations y sont traitées
			simultanément, dans une approche globale. C’est le siège de l’intuition et de la pensée divergente exempte
			d’ordre logique : l’enfant connaît la réponse d’un calcul mais ne peut justifier son résultat, ou donne des
			réponses hors sujet.</p>

		<p>A l’école, les enfants précoces se rendent compte qu’ils n’apprennent pas ce qu’ils croyaient, que les
			copains ne jouent pas comme eux, ne savent pas la même chose, ne parlent pas le même langage… Les réactions
			sont variées : ils peuvent s’ennuyer, rêver, se rebeller ou se faire oublier, parfois déprimer. Utilisant
			leur intuition et leurs facilités ils n’acquièrent pas la notion d’effort intellectuel. Leur relation à
			l’adulte et leur sensibilité peut les faire paraître impertinents ou provocateurs.</p>

		<p>Plus que les autres, ils ont besoin de donner du sens à leurs apprentissages et de travailler de façon
